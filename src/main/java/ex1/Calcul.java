package ex1;

/** Classe qui propose des opérations sur les nombres
 * 
 * @author Marlène
 * 
 */

public class Calcul {
	
	/** Retourne l'addition de deux entiers
	 * @param a entier 1
	 * @param b entier 2
	 * @return int
	 */

	public int addition(int a, int b){
		return a+b;
	}
	
	/** Retourne la soustraction de deux entiers
	 * @param a entier 1
	 * @param b entier 2
	 * @return int
	 */
	
	
	public int soustraction(int a, int b){
		return a-b;
	}
}
